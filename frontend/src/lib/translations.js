export function translateBenchmark(language, key, params = {}, fallback = '') {
  const template = BENCHMARK_TEXT[language]?.[key] || fallback
  return template.replace(/\{(\w+)\}/g, (match, name) => (
    params[name] == null ? match : String(params[name])
  ))
}
